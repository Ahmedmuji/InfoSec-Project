"""Aggregate every available result JSON/CSV into a markdown evaluation report.

Reads from ``results/`` and writes ``results/EVALUATION_REPORT.md`` with:
  1. Executive summary
  2. Classification performance table (vs paper baselines)
  3. Privacy analysis (sigma sweep)
  4. Communication efficiency (vs FL-LSTM)
  5. Limitations
  6. Conclusion
"""
from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any, Dict, List, Optional

import pandas as pd


# Paper baseline reference (Anwar et al. 2025, FL-LSTM on WSN-DS / CIC-IDS-2017
# / UNSW-NB15 — used solely for headline comparison context).
PAPER_BASELINE = {
    "accuracy_range": (0.981, 0.992),
    "f1_range": (0.978, 0.990),
    "fpr_range": (0.008, 0.021),
}


# ---------------------------------------------------------------------------
def _read_json(path: Path) -> Optional[Dict[str, Any]]:
    if not path.exists():
        return None
    try:
        return json.loads(path.read_text())
    except Exception as e:
        print(f"WARN: could not parse {path}: {e}")
        return None


def _fmt_pct(x: float) -> str:
    return f"{x * 100:.2f}%"


def _verdict_eps(eps: float) -> str:
    if eps <= 1.0:
        return "STRONG (eps <= 1.0)"
    if eps <= 3.0:
        return "MODERATE (1 < eps <= 3)"
    if eps <= 8.0:
        return "WEAK (3 < eps <= 8)"
    return "INSUFFICIENT (eps > 8)"


# ---------------------------------------------------------------------------
def build_report(results_dir: Path) -> str:
    primary_summary = (
        _read_json(results_dir / "ciciot_summary.json") or
        _read_json(results_dir / "summary_iid.json") or
        _read_json(results_dir / "summary.json")
    )
    baseline = _read_json(results_dir / "baseline_comparison.json")
    sweep_csv = results_dir / "privacy_accuracy_tradeoff.csv"
    sweep_df: Optional[pd.DataFrame] = pd.read_csv(sweep_csv) if sweep_csv.exists() else None

    parts: List[str] = []
    parts.append("# PrivFed-TCN Evaluation Report\n")
    parts.append("Auto-generated by `evaluation/generate_report.py`. "
                 "All numbers come straight from the persisted result files in "
                 "`results/`.\n")

    # ------------------- 1. Executive summary --------------------------
    parts.append("\n## 1. Executive Summary\n")
    if primary_summary is None:
        parts.append("> No primary summary JSON found — run `train.py` first.\n")
    else:
        m = primary_summary["final_test_metrics"]
        priv = primary_summary.get("privacy", {})
        eps = priv.get("epsilon", float("nan"))
        comm = primary_summary.get("communication", {})
        n_classes = len(primary_summary.get("class_names", []))
        parts.append(
            f"PrivFed-TCN achieved **accuracy {_fmt_pct(m['accuracy'])}**, "
            f"**F1-macro {_fmt_pct(m['f1_macro'])}**, "
            f"**FPR {_fmt_pct(m['fpr'])}**, and "
            f"**AUC-ROC {m.get('auc_roc', float('nan')):.4f}** "
            f"on {n_classes} class(es), against a base-paper FL-LSTM range of "
            f"accuracy {_fmt_pct(PAPER_BASELINE['accuracy_range'][0])}-"
            f"{_fmt_pct(PAPER_BASELINE['accuracy_range'][1])} and "
            f"F1 {_fmt_pct(PAPER_BASELINE['f1_range'][0])}-"
            f"{_fmt_pct(PAPER_BASELINE['f1_range'][1])}. "
            f"Cumulative privacy budget after training is "
            f"**eps = {eps:.4f}** (delta = {priv.get('delta', 1e-5)}). "
            f"Total communication: {comm.get('total_mb', 0):.2f} MB across "
            f"{comm.get('n_rounds', 0)} rounds.\n")

    # ------------------- 2. Classification performance ------------------
    parts.append("\n## 2. Classification Performance\n")
    if primary_summary is None:
        parts.append("_No data._\n")
    else:
        m = primary_summary["final_test_metrics"]
        rows = [
            ("Accuracy", PAPER_BASELINE["accuracy_range"], m["accuracy"]),
            ("F1 (macro)", PAPER_BASELINE["f1_range"], m["f1_macro"]),
            ("FPR", PAPER_BASELINE["fpr_range"], m["fpr"]),
        ]
        parts.append("| Metric | Base paper FL-LSTM | PrivFed-TCN | Delta |\n")
        parts.append("|---|---|---|---|\n")
        for name, paper_range, ours in rows:
            paper_mid = 0.5 * (paper_range[0] + paper_range[1])
            delta = ours - paper_mid
            sign = "+" if delta >= 0 else ""
            parts.append(
                f"| {name} | "
                f"{_fmt_pct(paper_range[0])} - {_fmt_pct(paper_range[1])} | "
                f"{_fmt_pct(ours)} | "
                f"{sign}{delta * 100:.2f} pp vs paper midpoint |\n")
        per_class = m.get("per_class_f1", [])
        names = primary_summary.get("class_names", [])
        if per_class and names:
            parts.append("\n### Per-class F1 (sorted)\n")
            parts.append("| Class | F1 |\n|---|---|\n")
            for cls, f1 in sorted(zip(names, per_class), key=lambda kv: -kv[1]):
                parts.append(f"| {cls} | {f1:.4f} |\n")

    # ------------------- 3. Privacy analysis ----------------------------
    parts.append("\n## 3. Privacy Analysis\n")
    if sweep_df is None or sweep_df.empty:
        parts.append("_No sigma sweep CSV — run with `--noise_multiplier_sweep`._\n")
    else:
        parts.append("| sigma | epsilon | accuracy | F1-macro | FPR | verdict |\n")
        parts.append("|---|---|---|---|---|---|\n")
        for _, r in sweep_df.iterrows():
            parts.append(
                f"| {r['noise_multiplier']:.2f} | {r['epsilon']:.4f} | "
                f"{_fmt_pct(r['accuracy'])} | {_fmt_pct(r['f1_macro'])} | "
                f"{_fmt_pct(r['fpr'])} | {_verdict_eps(float(r['epsilon']))} |\n")

    # ------------------- 4. Communication efficiency --------------------
    parts.append("\n## 4. Communication Efficiency\n")
    if baseline is None:
        parts.append("_No `baseline_comparison.json` — run with `--run_baseline_comparison`._\n")
    else:
        tcn = baseline["privfed_tcn"]; lstm = baseline["fl_lstm"]
        n_rounds = baseline.get("n_rounds", 50)
        parts.append("| Metric | PrivFed-TCN | FL-LSTM | Reduction |\n")
        parts.append("|---|---|---|---|\n")
        rows = [
            ("Parameters", tcn["param_count"], lstm["param_count"]),
            ("Model size (KB)", tcn["model_size_kb"], lstm["model_size_kb"]),
            ("MB / round", tcn["comm_mb_per_round"], lstm["comm_mb_per_round"]),
            (f"Total MB ({n_rounds} rounds)",
             tcn["comm_mb_per_round"] * n_rounds,
             lstm["comm_mb_per_round"] * n_rounds),
        ]
        for name, t, l in rows:
            red = (l - t) / l * 100 if l else 0.0
            parts.append(f"| {name} | {t:,.2f} | {l:,.2f} | {red:.2f}% |\n")
        parts.append(
            f"\n**Headline:** PrivFed-TCN reduces per-round communication by "
            f"**{baseline.get('comm_reduction_pct', 0):.2f}%** "
            f"vs the FL-LSTM baseline.\n")

    # ------------------- 5. Limitations ---------------------------------
    parts.append("\n## 5. Limitations\n")
    issues: List[str] = []
    if primary_summary is not None:
        m = primary_summary["final_test_metrics"]
        eps = primary_summary.get("privacy", {}).get("epsilon", float("inf"))
        if m["accuracy"] < 0.98:
            issues.append("- Accuracy below the 98% paper target.")
        if m["fpr"] > 0.02:
            issues.append("- FPR exceeds the 2% paper target.")
        if eps > 1.0:
            issues.append(f"- Privacy budget eps={eps:.2f} exceeds the eps<=1.0 target. "
                          "Re-run with `--auto_calibrate_epsilon`.")
        if len(primary_summary.get("class_names", [])) < 10:
            issues.append("- Fewer than 10 attack classes — re-run on CIC-IoT-2023 "
                          "for full attack coverage.")
    if not issues:
        issues.append("- All declared targets met on the available data.")
    parts.extend(issues)
    parts.append("\n_Note: dataset differences (CIC-IoT-2023 vs the WSN-DS / CIC-IDS-2017 / "
                 "UNSW-NB15 used in the base paper) make absolute metric comparisons "
                 "indicative rather than strict._\n")

    # ------------------- 6. Conclusion ----------------------------------
    parts.append("\n## 6. Conclusion\n")
    if primary_summary is not None:
        m = primary_summary["final_test_metrics"]
        eps = primary_summary.get("privacy", {}).get("epsilon", float("nan"))
        red = baseline.get("comm_reduction_pct", 0.0) if baseline else 0.0
        parts.append(
            f"PrivFed-TCN delivers {_fmt_pct(m['accuracy'])} accuracy with "
            f"{_fmt_pct(m['fpr'])} FPR while spending eps={eps:.2f} of privacy budget. "
            f"It transmits {red:.1f}% less data per round than the FL-LSTM baseline. "
            f"The combination of compactness, formal DP guarantees, and local SHAP "
            f"explainability positions PrivFed-TCN as a strong candidate for "
            f"on-device IoT intrusion detection.\n")
    else:
        parts.append("Insufficient results to draw a conclusion.\n")

    return "".join(parts)


# ---------------------------------------------------------------------------
def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--results_dir", required=True)
    p.add_argument("--output", default=None,
                   help="Path to write the report (defaults to results_dir/EVALUATION_REPORT.md)")
    args = p.parse_args()

    results_dir = Path(args.results_dir)
    out_path = Path(args.output) if args.output else results_dir / "EVALUATION_REPORT.md"
    text = build_report(results_dir)
    out_path.write_text(text, encoding="utf-8")
    print(f"Report written -> {out_path}")


if __name__ == "__main__":
    main()
